// 문제
// 두 정수 A와 B를 입력받은 다음, A/B를 출력하는 프로그램을 작성하시오.
//
// 입력
// 첫째 줄에 A와 B가 주어진다. (0 < A, B < 10)
//
// 출력
// 첫째 줄에 A/B를 출력한다. 실제 정답과 출력값의 절대오차 또는 상대오차가 10-9 이하이면 정답이다.

#include <stdio.h>

int main(void)
{
    int a, b;

    scanf("%d %d", &a, &b);
    printf("%.9lf", (double)a / b);
    return (0);
}

// Tip: %.9g가 안되는 이유는 .9는 소수점 아래 9자리까지 표현하는 것이 아니라, 가수부의 개수를 9개로 제한하여 표현하는 것.
//      예를 들어 11.1234567891 경우, %.9f는 11.123456789이 나오고, %.9g는 11.1234568(반올림)이 출력됨.
